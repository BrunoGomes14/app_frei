package com.nsf.appfreiinscricoes.model;

public class TbCodigo
{
    int idCodigo;
    int dsAno;
    int nrCod;
    int dsConfirmacao;

    public int getIdCodigo() {
        return idCodigo;
    }

    public void setIdCodigo(int idCodigo) {
        this.idCodigo = idCodigo;
    }

    public int getDsAno() {
        return dsAno;
    }

    public void setDsAno(int dsAno) {
        this.dsAno = dsAno;
    }

    public int getNrCod() {
        return nrCod;
    }

    public void setNrCod(int nrCod) {
        this.nrCod = nrCod;
    }

    public int isDsConfirmacao() {
        return dsConfirmacao;
    }

    public void setDsConfirmacao(int dsConfirmacao) {
        this.dsConfirmacao = dsConfirmacao;
    }
}
